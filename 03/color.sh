#!/bin/bash

if [ $# -ne 4 ]; then
  echo "Ошибка: необходимо ровно 4 параметра."
  echo "Использование: $0 <фон_имён> <шрифт_имён> <фон_значений> <шрифт_значений>"
  echo "Цвета: 1-белый, 2-красный, 3-зелёный, 4-синий, 5-фиолетовый, 6-чёрный"
  exit 1
fi

for param in "$@"; do
  if ! [[ "$param" =~ ^[1-6]$ ]]; then
    echo "Ошибка: параметр \"$param\" не входит в диапазон 1-6."
    echo "Запустите скрипт повторно с корректными параметрами."
    exit 1
  fi
done

if [ "$1" -eq "$2" ]; then
  echo "Ошибка: цвет фона ($1) и цвет шрифта ($2) столбца названий совпадают."
  echo "Запустите скрипт повторно с разными значениями параметров 1 и 2."
  exit 1
fi

if [ "$3" -eq "$4" ]; then
  echo "Ошибка: цвет фона ($3) и цвет шрифта ($4) столбца значений совпадают."
  echo "Запустите скрипт повторно с разными значениями параметров 3 и 4."
  exit 1
fi

# --- Таблица цветов ---
# 1-белый, 2-красный, 3-зелёный, 4-синий, 5-фиолетовый, 6-чёрный

get_fg() {
  case $1 in
    1) echo "97" ;;  # белый
    2) echo "31" ;;  # красный
    3) echo "32" ;;  # зелёный
    4) echo "34" ;;  # синий
    5) echo "35" ;;  # фиолетовый
    6) echo "30" ;;  # чёрный
  esac
}

get_bg() {
  case $1 in
    1) echo "107" ;; # белый
    2) echo "41"  ;; # красный
    3) echo "42"  ;; # зелёный
    4) echo "44"  ;; # синий
    5) echo "45"  ;; # фиолетовый
    6) echo "40"  ;; # чёрный
  esac
}

BG_NAME=$(get_bg "$1")
FG_NAME=$(get_fg "$2")
BG_VAL=$(get_bg "$3")
FG_VAL=$(get_fg "$4")

NAME_STYLE="\033[${BG_NAME};${FG_NAME}m"
VAL_STYLE="\033[${BG_VAL};${FG_VAL}m"
RESET="\033[0m"